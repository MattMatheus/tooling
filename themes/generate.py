# =============================================================================
# Registry — add new tools here
# =============================================================================

RENDERERS: dict[str, callable] = {
    "ghostty": render_ghostty,
    "zed":     render_zed,
    "obsidian": render_obsidian,
    # "btop":   render_btop,
    # "delta":  render_delta,
    # "bat":    render_bat,
    # "starship": render_starship,
}


# =============================================================================
# Main
# =============================================================================

def main() -> None:
    meta, colors = load_palette()
    out_dir = SCRIPT_DIR

    requested = sys.argv[1:] or list(RENDERERS)
    unknown = set(requested) - set(RENDERERS)
    if unknown:
        print(f"Unknown target(s): {', '.join(sorted(unknown))}")
        print(f"Available: {', '.join(sorted(RENDERERS))}")
        sys.exit(1)

    print(f"Generating palette '{meta['name']}' ({meta['variant']})...")
    for target in requested:
        RENDERERS[target](colors, meta, out_dir)
    print("Done.")


if __name__ == "__main__":
    main()
