#!/usr/bin/env python3
"""
Theme generator — reads palette.toml and writes all target configs.

Usage:
    python3 generate.py           # generate all targets
    python3 generate.py ghostty   # generate specific target
    python3 generate.py zed

Adding a new tool: write a render_<toolname>(p, meta, out_dir) function
and register it in RENDERERS at the bottom.
"""

import json
import sys
import textwrap
import tomllib
from string import Template
from pathlib import Path

SCRIPT_DIR = Path(__file__).parent
PALETTE_FILE = SCRIPT_DIR / "palette.toml"


# =============================================================================
# Helpers
# =============================================================================

def load_palette() -> tuple[dict, dict]:
    with open(PALETTE_FILE, "rb") as f:
        data = tomllib.load(f)

    if "meta" not in data or "colors" not in data:
        print(f"Invalid palette format in {PALETTE_FILE}.")
        print("Expected keys: [meta] and [colors] tables.")
        print("Example:")
        print('[meta]')
        print('name = "calm"')
        print('[colors]')
        sys.exit(1)

    return data["meta"], data["colors"]


def hex_to_rgb(h: str) -> tuple[int, int, int]:
    h = h.lstrip("#")
    return int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)


def strip_hex(h: str) -> str:
    """Return hex without leading #."""
    return h.lstrip("#")


def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)
    print(f"  wrote {path.relative_to(SCRIPT_DIR)}")


# =============================================================================
# Ghostty renderer
# The theme file is a subset of ghostty config — only color keys.
# Loaded before user config; user config overrides on conflict.
# Ref: https://ghostty.org/docs/features/theme
# =============================================================================

def render_ghostty(p: dict, meta: dict, out_dir: Path) -> None:
    name = meta["name"]

    # Ghostty ANSI palette indices 0-15:
    # 0  black (normal)    8  bright black
    # 1  red               9  bright red
    # 2  green            10  bright green
    # 3  yellow           11  bright yellow
    # 4  blue             12  bright blue
    # 5  magenta          13  bright magenta
    # 6  cyan             14  bright cyan
    # 7  white            15  bright white
    lines = [
        f"# Calm — generated by themes/generate.py",
        f"# Edit palette.toml, not this file.",
        f"",
        f"background = {strip_hex(p['bg1'])}",
        f"foreground = {strip_hex(p['fg0'])}",
        f"",
        f"cursor-color = {strip_hex(p['accent'])}",
        f"selection-background = {strip_hex(p['bg3'])}",
        f"selection-foreground = {strip_hex(p['fg0'])}",
        f"",
        f"# Normal (0-7)",
        f"palette = 0={p['bg3']}",     # black  → dark surface (visible on bg)
        f"palette = 1={p['red']}",
        f"palette = 2={p['green']}",
        f"palette = 3={p['yellow']}",
        f"palette = 4={p['blue']}",
        f"palette = 5={p['magenta']}",
        f"palette = 6={p['cyan']}",
        f"palette = 7={p['fg1']}",     # white  → muted fg
        f"",
        f"# Bright (8-15)",
        f"palette = 8={p['bg4']}",     # bright black → slightly lighter surface
        f"palette = 9={p['red']}",
        f"palette = 10={p['green']}",
        f"palette = 11={p['yellow']}",
        f"palette = 12={p['accent']}",
        f"palette = 13={p['magenta']}",
        f"palette = 14={p['cyan']}",
        f"palette = 15={p['fg0']}",    # bright white → primary fg
    ]

    write(out_dir / "ghostty" / f"{name}.theme", "\n".join(lines) + "\n")


